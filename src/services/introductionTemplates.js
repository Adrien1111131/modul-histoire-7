/**
 * Génère une introduction aléatoire et variée
 * @param {Object} options - Options pour la génération
 * @returns {string} Introduction générée
 */
export const generateIntroduction = (options = {}) => {
  const {
    type = 'rencontre',
    dominantStyle = 'visuel',
    name = '',
    situation = '',
    personnage = '',
    lieu = ''
  } = options;
  
  // Sélectionner une approche aléatoire
  const approaches = ['temporelle', 'spatiale', 'émotionnelle', 'sensorielle', 'actionnelle'];
  const approach = approaches[Math.floor(Math.random() * approaches.length)];
  
  // Sélectionner un template aléatoire pour le type d'histoire et l'approche
  const templates = introTemplates[type]?.[approach] || introTemplates.rencontre.temporelle;
  const template = templates[Math.floor(Math.random() * templates.length)];
  
  // Sélectionner des éléments contextuels aléatoires
  const timeContext = timeContexts[Math.floor(Math.random() * timeContexts.length)];
  const locationDescription = lieu ? 
    `dans ${lieu}...` : 
    locationDescriptions[Math.floor(Math.random() * locationDescriptions.length)];
  const tensionTrigger = tensionTriggers[Math.floor(Math.random() * tensionTriggers.length)];
  
  // Sélectionner une description sensorielle adaptée au style dominant
  const sensoryStyle = dominantStyle.toLowerCase();
  const sensoryOptions = sensoryDescriptions[sensoryStyle] || sensoryDescriptions.visuel;
  const sensoryDescription = sensoryOptions[Math.floor(Math.random() * sensoryOptions.length)];
  
  // Sélectionner une approche d'ouverture aléatoire
  const openingApproach = openingApproaches[Math.floor(Math.random() * openingApproaches.length)];
  
  // Remplacer les placeholders dans le template
  let introduction = template
    .replace('TIME_CONTEXT', timeContext)
    .replace('LOCATION_DESCRIPTION', locationDescription)
    .replace('TENSION_TRIGGER', tensionTrigger)
    .replace('SENSORY_DESCRIPTION', sensoryDescription);
  
  // Ajouter des éléments spécifiques si fournis
  if (personnage) {
    introduction = introduction.replace(/tu t'affaires/g, `${personnage} s'affaire`);
    introduction = introduction.replace(/tu accomplis/g, `${personnage} accomplit`);
    
    // Ajouter une description du personnage
    const personnageDescriptions = [
      `${personnage}, avec cette présence qui fait toujours frémir...`,
      `${personnage}, dont le regard a toujours cet effet particulier...`,
      `${personnage}, qui sait exactement comment éveiller les sens...`,
      `${personnage}, avec cette façon unique de créer une atmosphère sensuelle...`
    ];
    
    const personnageDesc = personnageDescriptions[Math.floor(Math.random() * personnageDescriptions.length)];
    
    // Insérer la description du personnage à un endroit approprié
    const parts = introduction.split('.');
    if (parts.length > 2) {
      parts.splice(2, 0, ` ${personnageDesc}.`);
      introduction = parts.join('.');
    } else {
      introduction += ` ${personnageDesc}`;
    }
  }
  
  if (situation) {
    // Ajouter un élément de situation
    const situationDescriptions = [
      `Cette situation de ${situation} crée une tension particulière.`,
      `Dans ce contexte de ${situation}, les barrières habituelles tombent une à une.`,
      `${situation} nous place dans une dynamique différente, plus intense.`,
      `Cette ambiance de ${situation} change complètement notre façon d'interagir.`
    ];
    
    const situationDesc = situationDescriptions[Math.floor(Math.random() * situationDescriptions.length)];
    
    // Insérer la description de la situation à un endroit approprié
    const parts = introduction.split('.');
    if (parts.length > 3) {
      parts.splice(3, 0, ` ${situationDesc}.`);
      introduction = parts.join('.');
    } else {
      introduction += ` ${situationDesc}`;
    }
  }
  
  // Personnaliser avec le nom si fourni
  if (name) {
    introduction = introduction.replace(/tu /g, `${name} `);
    introduction = introduction.replace(/ tu/g, ` ${name}`);
    introduction = introduction.replace(/tes /g, `tes `);
    introduction = introduction.replace(/ton /g, `ton `);
  }
  
  // Éviter les répétitions de "Hey" et "c'est moi"
  introduction = introduction.replace(/^Hey/g, openingApproach);
  introduction = introduction.replace(/c'est moi/g, "je suis là");
  introduction = introduction.replace(/t'entends ma voix/g, "ma présence te fait réagir");
  introduction = introduction.replace(/là, tout près de toi/g, "dans cet espace que nous partageons");
  
  return introduction;
};

/**
 * Génère une structure d'introduction complète avec les 4 parties
 * @param {Object} options - Options pour la génération
 * @returns {string} Structure d'introduction complète
 */
export const generateCompleteIntroduction = (options = {}) => {
  const {
    type = 'rencontre',
    dominantStyle = 'visuel',
    name = '',
    situation = '',
    personnage = '',
    lieu = ''
  } = options;
  
  // Générer l'introduction de base
  const baseIntro = generateIntroduction({ type, dominantStyle, name });
  
  // Ajouter des détails spécifiques si fournis
  let detailedIntro = baseIntro;
  
  if (lieu) {
    detailedIntro = detailedIntro.replace(/dans .*?\.\.\./, `dans ${lieu}...`);
  }
  
  if (personnage) {
    // Ajouter une description du personnage
    const personnageDescriptions = [
      `${personnage}, avec cette présence qui te fait toujours frémir...`,
      `${personnage}, dont le regard a toujours eu cet effet sur toi...`,
      `${personnage}, qui sait exactement comment te faire réagir...`,
      `${personnage}, avec cette façon unique de te faire sentir désirée...`
    ];
    
    const personnageDesc = personnageDescriptions[Math.floor(Math.random() * personnageDescriptions.length)];
    detailedIntro += ` ${personnageDesc}`;
  }
  
  if (situation) {
    // Ajouter un élément de situation
    const situationDescriptions = [
      `Cette situation de ${situation} crée une tension particulière entre nous.`,
      `Dans ce contexte de ${situation}, les barrières habituelles tombent une à une.`,
      `${situation} nous place dans une dynamique différente, plus intense.`,
      `Cette ambiance de ${situation} change complètement notre façon d'interagir.`
    ];
    
    const situationDesc = situationDescriptions[Math.floor(Math.random() * situationDescriptions.length)];
    detailedIntro += ` ${situationDesc}`;
  }
  
  // Structure complète en 4 parties
  const completeIntroduction = `
${detailedIntro}

Je prends le temps d'observer ce qui se passe entre nous, cette tension qui monte progressivement. Chaque regard, chaque geste semble chargé d'une intention nouvelle. L'air autour de nous devient électrique, presque palpable.

Quelque chose a changé dans notre façon d'être ensemble. Cette énergie entre nous, impossible à ignorer maintenant. Je m'approche de toi, lentement, délibérément, te laissant le temps de comprendre où tout cela nous mène.

Nos corps ne se touchent pas encore, mais c'est comme si chaque centimètre qui nous sépare était déjà chargé de promesses. Je peux voir dans tes yeux que tu ressens la même chose, cette anticipation délicieuse qui fait battre ton cœur plus vite...
`;
  
  return completeIntroduction.trim();
};

